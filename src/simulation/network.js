// Dijkstra's Shortest Path Algorithm
export function findShortestPath(nodes, edges, startId, endId) {
  if (!nodes || !nodes[startId] || !nodes[endId]) return null;

  const distances = {};
  const previous = {};
  const queue = new Set();

  Object.keys(nodes).forEach(nodeId => {
    distances[nodeId] = Infinity;
    previous[nodeId] = null;
    queue.add(nodeId);
  });

  distances[startId] = 0;

  // Build adjacency list
  const adj = {};
  Object.keys(nodes).forEach(nodeId => { adj[nodeId] = []; });
  edges.forEach(edge => {
    // Travel time as weight: distance / speedLimit
    if (!nodes[edge.from] || !nodes[edge.to]) return;
    const dist = getDistance(nodes[edge.from].lat, nodes[edge.from].lng, nodes[edge.to].lat, nodes[edge.to].lng);
    const weight = dist / (edge.speedLimit || 40);
    
    adj[edge.from].push({ to: edge.to, weight });
    if (edge.bidir) {
      adj[edge.to].push({ to: edge.from, weight });
    }
  });

  while (queue.size > 0) {
    // Find node with minimum distance
    let u = null;
    queue.forEach(nodeId => {
      if (u === null || distances[nodeId] < distances[u]) {
        u = nodeId;
      }
    });

    if (u === null || distances[u] === Infinity || u === endId) {
      break;
    }

    queue.delete(u);

    adj[u].forEach(neighbor => {
      if (!queue.has(neighbor.to)) return;
      const alt = distances[u] + neighbor.weight;
      if (alt < distances[neighbor.to]) {
        distances[neighbor.to] = alt;
        previous[neighbor.to] = u;
      }
    });
  }

  if (distances[endId] === Infinity) return null;

  const path = [];
  let curr = endId;
  while (curr !== null) {
    path.unshift(curr);
    curr = previous[curr];
  }
  return path;
}

// Compute the traffic flow state on all edges based on demands
export function simulateTraffic(nodes, edges, demands, trafficMode, newRoad = null) {
  // Filter out edges that refer to non-existent nodes to prevent crashes
  const validEdges = (edges || []).filter(e => nodes && nodes[e.from] && nodes[e.to]);

  // Deep clone edges and initialize flow variables
  const networkEdges = validEdges.map(e => ({
    ...e,
    currentFlow: 8 + Math.random() * 6, // Add baseline local traffic flow (8-14 units) so cars travel on all routes
    currentDensity: 0,
    currentSpeed: e.speedLimit,
    congestionColor: "#10b981", // Green default
    distance: getDistance(nodes[e.from].lat, nodes[e.from].lng, nodes[e.to].lat, nodes[e.to].lng)
  }));

  // Add the newly constructed road if it exists and connects valid nodes
  if (newRoad && nodes[newRoad.from] && nodes[newRoad.to]) {
    const newEdge = {
      from: newRoad.from,
      to: newRoad.to,
      name: "New Drawn Road",
      lanes: newRoad.lanes || 2,
      speedLimit: newRoad.speedLimit || 50,
      bidir: true,
      currentFlow: 0,
      currentDensity: 0,
      currentSpeed: newRoad.speedLimit || 50,
      congestionColor: "#10b981",
      distance: getDistance(nodes[newRoad.from].lat, nodes[newRoad.from].lng, nodes[newRoad.to].lat, nodes[newRoad.to].lng),
      isNew: true
    };
    networkEdges.push(newEdge);
  }

  // Adjust demand for Best (0.60), Normal (1.0), or Worst Case (1.40)
  let demandMultiplier = 1.0;
  if (trafficMode === "best") {
    demandMultiplier = 0.60;
  } else if (trafficMode === "worst") {
    demandMultiplier = 1.40;
  }
  const simulatedDemands = demands.map(d => ({
    ...d,
    volume: d.volume * demandMultiplier
  }));

  // Helper adjacency mapping for speed lookup and routing
  const findEdgeIndex = (fromId, toId) => {
    return networkEdges.findIndex(e => 
      (e.from === fromId && e.to === toId) || 
      (e.bidir && e.from === toId && e.to === fromId)
    );
  };

  // Route each demand and accumulate flow on edges
  simulatedDemands.forEach(demand => {
    const path = findShortestPath(nodes, networkEdges, demand.from, demand.to);
    if (!path || path.length < 2) return;

    for (let i = 0; i < path.length - 1; i++) {
      const u = path[i];
      const v = path[i+1];
      const edgeIdx = findEdgeIndex(u, v);
      if (edgeIdx !== -1) {
        // Accumulate directional traffic volume
        networkEdges[edgeIdx].currentFlow += demand.volume;
      }
    }
  });

  // Calculate density, speeds and colors
  // Capacity of 1 lane is defined as ~60 units of traffic volume.
  // Bottlenecks are emphasized at intersections, particularly where the new road merges
  networkEdges.forEach(edge => {
    const laneCapacity = 60 * edge.lanes;
    
    // Check if it's connected to the new road (bottleneck hotspots)
    let bottleneckFactor = 1.0;
    if (newRoad) {
      const isNewRoadConnection = 
        edge.from === newRoad.from || edge.to === newRoad.from ||
        edge.from === newRoad.to || edge.to === newRoad.to;
      
      if (isNewRoadConnection) {
        if (trafficMode === "worst") {
          bottleneckFactor = 1.5;
        } else if (trafficMode === "normal") {
          bottleneckFactor = 1.2;
        }
      }
    }

    edge.currentDensity = (edge.currentFlow / laneCapacity) * bottleneckFactor;
    
    // Greenshields speed-density relationship
    // Speed decreases as density goes up. Minimizing to 5 km/h at extreme density
    const speedRatio = Math.max(0.08, 1 - Math.pow(edge.currentDensity, 1.8));
    edge.currentSpeed = edge.speedLimit * speedRatio;

    // Categorize congestion
    // Green (0-35% density), Yellow (35-70% density), Red (70-95% density), Dark Red (>= 95% density)
    const density = edge.currentDensity;
    if (density < 0.35) {
      edge.congestionColor = "#10b981"; // Vibrant Emerald Green
    } else if (density < 0.70) {
      edge.congestionColor = "#f59e0b"; // Warm Amber Yellow
    } else if (density < 0.95) {
      edge.congestionColor = "#ef4444"; // Soft Red
    } else {
      // Bottleneck Alert
      edge.congestionColor = (trafficMode === "worst") ? "#7f1d1d" : "#ef4444"; // Dark Red in worst case
    }
  });

  return networkEdges;
}

// Calculate the simulation KPIs (Average Speed, Delay Time, and Congestion Index)
export function calculateKPIs(nodes, edges, demands, trafficMode, newRoad = null) {
  // Run simulated traffic for baseline (without new road)
  const baselineEdges = simulateTraffic(nodes, edges, demands, trafficMode, null);
  // Run simulated traffic for proposed case (with new road)
  const currentEdges = simulateTraffic(nodes, edges, demands, trafficMode, newRoad);

  // 1. Average Speed (weighted by road lengths and flow)
  let baselineTotalSpeedLength = 0;
  let baselineTotalLength = 0;
  baselineEdges.forEach(edge => {
    if (edge.currentFlow > 0) {
      baselineTotalSpeedLength += edge.currentSpeed * edge.distance * edge.currentFlow;
      baselineTotalLength += edge.distance * edge.currentFlow;
    }
  });
  const baselineAvgSpeed = baselineTotalLength > 0 ? (baselineTotalSpeedLength / baselineTotalLength) : 45;

  let currentTotalSpeedLength = 0;
  let currentTotalLength = 0;
  currentEdges.forEach(edge => {
    if (edge.currentFlow > 0) {
      currentTotalSpeedLength += edge.currentSpeed * edge.distance * edge.currentFlow;
      currentTotalLength += edge.distance * edge.currentFlow;
    }
  });
  const currentAvgSpeed = currentTotalLength > 0 ? (currentTotalSpeedLength / currentTotalLength) : 45;

  // Calculate percentage speed change
  const speedChangePct = ((currentAvgSpeed - baselineAvgSpeed) / baselineAvgSpeed) * 100;

  // 2. Travel Time & Daily Delay Saved
  // Delay saved (hours) = Sum [ demand_vol * (baseline_travel_time - current_travel_time) ]
  let totalDelaySavedHrs = 0;
  let demandMultiplier = 1.0;
  if (trafficMode === "best") {
    demandMultiplier = 0.60;
  } else if (trafficMode === "worst") {
    demandMultiplier = 1.40;
  }
  
  demands.forEach(demand => {
    const vol = demand.volume * demandMultiplier;
    
    // Baseline path
    const pathBase = findShortestPath(nodes, baselineEdges, demand.from, demand.to);
    let timeBase = 0;
    if (pathBase) {
      for (let i = 0; i < pathBase.length - 1; i++) {
        const u = pathBase[i];
        const v = pathBase[i+1];
        const e = baselineEdges.find(edge => 
          (edge.from === u && edge.to === v) || (edge.bidir && edge.from === v && edge.to === u)
        );
        if (e) timeBase += e.distance / e.currentSpeed; // hours
      }
    }

    // Proposed path
    const pathCurr = findShortestPath(nodes, currentEdges, demand.from, demand.to);
    let timeCurr = 0;
    if (pathCurr) {
      for (let i = 0; i < pathCurr.length - 1; i++) {
        const u = pathCurr[i];
        const v = pathCurr[i+1];
        const e = currentEdges.find(edge => 
          (edge.from === u && edge.to === v) || (edge.bidir && edge.from === v && edge.to === u)
        );
        if (e) timeCurr += e.distance / e.currentSpeed;
      }
    }

    if (timeBase > timeCurr) {
      // Accumulate delay saved per hour. Let's scale up to daily savings (say, 8 peak hours equivalent)
      totalDelaySavedHrs += vol * (timeBase - timeCurr) * 8; 
    }
  });

  // 3. Congestion Index (A - F)
  // Ratio of actual speed to speed limit on average across the entire network
  let ratioSum = 0;
  let flowSum = 0;
  currentEdges.forEach(e => {
    ratioSum += (e.currentSpeed / e.speedLimit) * e.currentFlow;
    flowSum += e.currentFlow;
  });
  const avgSpeedRatio = flowSum > 0 ? (ratioSum / flowSum) : 0.8;

  let congestionGrade = "A";
  if (avgSpeedRatio > 0.80) congestionGrade = "A";
  else if (avgSpeedRatio > 0.65) congestionGrade = "B";
  else if (avgSpeedRatio > 0.50) congestionGrade = "C";
  else if (avgSpeedRatio > 0.35) congestionGrade = "D";
  else if (avgSpeedRatio > 0.20) congestionGrade = "E";
  else congestionGrade = "F";

  return {
    avgSpeed: currentAvgSpeed,
    speedChangePct: speedChangePct,
    delaySavedHrs: Math.max(0, totalDelaySavedHrs),
    congestionGrade: congestionGrade
  };
}
